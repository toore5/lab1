print('Програма обчислить вираз в залежності від введених значень змінних a та b. Змінні обов\'язково повинні бути додатніми.\n')
a = int(input('Введіть значення a: '))
while a<0:
    a = int(input('Введіть корректне значення a: '))    
b = int(input('Введіть значення b: '))
while b<0:
    b = int(input('Введіть корректне значення b: '))
if a>b:
    X = b*a+1
elif a==b:
    X = -10
else:
    X = (a/5)-b
print('Результат: ', X)
